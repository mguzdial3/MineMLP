			timer+=Time.deltaTime;
		}
		else{
			timer=0;

			//Get Action Based on Index
			double max = double.NegativeInfinity;
			int choice = -1;

			//Get max
			for(int i = 0; i<4; i++){
				if(policy[currIndex,i]>max){
					choice = i;
					max = policy[currIndex,i];
				}
			}
			int blockId = Map.Instance.GetBlockInt((int)transform.position.x,groundLevel,(int)transform.position.z);

			if(blockId==4 || blockId == 5){
				float random = Random.Range(0f,1f);
				if(random<0.25f){
					choice=0;
				}
				else if(random<0.5f){
					choice=1;
				}
				else if(random<0.75f){
					choice=2;
				}
				else if(random<1f){
					choice=3;
				}

			}
			else{
				float random = Random.Range(0f,6f);
				random = 2;//TODO; remove, this is for testing
				if(random>1){
					//do smart move
				}
				else{
					if(random<0.25f){
						choice=0;
					}
					else if(random<0.5f){
						choice=1;
					}
					else if(random<0.75f){
						choice=2;
					}
					else if(random<1f){
						choice=3;
					}
				}
			}

			if(choice!=-1){
				currAction = choice;
				Vector3 newPos =transform.position;
				if(choice==0){ //Down
					newPos+=Vector3.forward;
				}
				else if(choice==1){//Up
					newPos+=Vector3.back;
				}
				else if(choice==2){//Right
					newPos+=Vector3.right;
				}
				else if(choice==3){//Left
					newPos+=Vector3.left;
				}

				if(Map.Instance.GetBlockInt((int)newPos.x,(int)newPos.y,(int)newPos.z)!=2 && GetIndexByPosition(newPos)>0){
					transform.position = newPos;
					prevIndex = currIndex;
					currIndex = GetIndexByPosition(transform.position);
				}

			}
		}
	}

	public int GetIndexByPosition(Vector3 pos){
		int xPos = (int)pos.x;
		int zPos = (int)pos.z;

		int xIndex = xPos-Map.Instance.xMin-startingIndex;
		int zIndex = zPos -Map.Instance.zMin - startingIndex;
		int index = zIndex*width+xIndex;

		return index;
	}

	public int GetCurrentIndex(){
		return currIndex;
	}

	public int GetCurrentAction(){
		return currAction;
	}

	public void ChangeCurrActionPolicy(double amount){
		policy[prevIndex,currAction]+=amount;
	}

	void OnDestroy(){
		string policyRep = "{";
		for(int i = 0; i<policy.GetLength(0); i++){
			policyRep+="{";
			for(int j = 0; j<policy.GetLength(1); j++){
				policyRep+=" "+policy[i,j];
				if(j!=policy.GetLength(1)-1){
					policyRep+=",";
				}

			}
			if(i!=policy.GetLength(0)-1){
				policyRep+="},\n";
			}
			else{
				policyRep+="}};";
			}

		}
		System.IO.File.WriteAllText(START_OF_TEXT+END_OF_TEXT, policyRep);
	}

	void OnCollisionEnter(Collision other){
		if(other.collider.tag=="NPC"){
			Renderer[] renderers = other.collider.GetComponentsInChildren<Renderer>();

			foreach(Renderer r in renderers){
				r.enabled=false;
			}
		}
	}


